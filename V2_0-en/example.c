/**
 * @brief Application Entry Point
 * @note  This example demonstrates a typical use case of two Modbus instances
 * sharing a data source.
 */
int main(void) {
  /* MCU initialization (code generated by CubeMX) */
  HAL_Init();
  // SystemClock_Config();
  // MX_GPIO_Init();
  // MX_USART1_UART_Init();
  // MX_USART2_UART_Init();

  /* Initialize all Modbus instances */
  if (!MB_AppInit()) {
    Error_Handler();
  }

  /* Initialize shared data (example) */
  g_holding_regs[0] = 0x1234;
  g_holding_regs[1] = 0x5678;
  g_input_regs[0] = 0;

  /* Main loop */
  uint32_t tick_count = 0;

  while (1) {
    /* ==================== Modbus Protocol Processing =================== */
    /* Two instances work in parallel, processing the data received from their
     * respective UARTs */
    MB_ServerProcessADU(&hmb1);
    MB_ServerProcessADU(&hmb2);

    /* ==================== Application Logic Example =================== */

    /*
     * Example 1: Simulated sensor data update (updates every 1000 cycles)
     * Note : If high data consistency is required,
     *        it is recommended to use critical section protection.
     */
    /**
     * __disable_irq();
     * g_input_regs[0] = new_value;
     * __enable_irq();
     */
    if (++tick_count >= 1000) {
      tick_count = 0;

      /* Simulate ADC sampled values ​​*/
      g_input_regs[0]++; /* 16-bit write, Cortex-M guarantees atomicity */

      /* Synchronize the value of register 0 to input register 1 */
      g_input_regs[1] = g_holding_regs[0];
    }

    /* Example 2: Using coil 0 to control an LED (assuming PC13 is connected to
     * the LED) */
    if (g_coils[0] & 0x01) {
      HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_RESET); /* LED ON */
    } else {
      HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_SET); /* LED OFF */
    }

    /**
     * Example 3: Handling configuration update requests
     * The 0x64 function code callback only sets the flag; the actual Flash
     * operation is executed here. This avoids time-consuming Flash operations
     * in the interrupt context.
     */
    if (g_config_update_pending) {
      g_config_update_pending = false;

      /* Update configuration structure */
      g_flash_config.server_addr1 = hmb1.server_addr;
      g_flash_config.baud_rate1 = hmb1.baud_rate;
      g_flash_config.parity1 = hmb1.parity;

      g_flash_config.server_addr2 = hmb2.server_addr;
      g_flash_config.baud_rate2 = hmb2.baud_rate;
      g_flash_config.parity2 = hmb2.parity;

      /**
       *  TODO: Save configuration to Flash
       * Flash_SaveConfig(&g_flash_config);
       *
       * Restart after delay:
       * HAL_Delay(50);
       * NVIC_SystemReset();
       */
    }

    /* Other application logic... */
  }
}

