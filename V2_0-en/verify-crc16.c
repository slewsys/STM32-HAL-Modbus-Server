/**
 * modbus_crc16_init: Generate Modbus CRC16 lookup table.
 */
void modbus_crc16_init(void) {
  for (uint16_t crc, i = 0; i < 256; i++) {
    crc = i;
    for (uint8_t j = 0; j < 8; j++) {
      crc = (crc & 0x0001) ? (crc >> 1) ^ 0xA001 : (crc >> 1);
    }
    crc_table[i] = crc;
  }
}

/**
 * modbus_crc16: Calculate Modbus CRC16 by lookup table.
 */
uint16_t modbus_crc16(const uint8_t *data, size_t length) {
  uint16_t crc = 0xFFFF;
  uint8_t table_index;

  for (size_t i = 0; i < length; i++) {
    table_index = (uint8_t)((crc ^ data[i]) & 0xFF);

    crc >>= 8;
    crc ^= crc_table[table_index];
  }

  return crc;
}

/**
 * modbus_crc16_long: Calculate Modbus CRC16 without lookup table.
 */
uint16_t modbus_crc16_long(const uint8_t *data, size_t length) {
  uint16_t crc = 0xFFFF; // Initial value

  for (uint16_t i = 0; i < length; i++) {
    crc ^= data[i];
    for (uint8_t j = 0; j < 8; j++) {
      crc = crc & 0x0001 ? (crc >> 1) ^ 0xA001 : crc >> 1;
    }
  }

  return crc;
}
